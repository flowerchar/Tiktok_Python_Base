name_list = ['TOM', 'Lily', 'ROSE']

'''
1. 准备表示下标数据
2. 循环while
    条件 i < 3  len()
    遍历：依次按顺序访问到序列的每一个数据 
    i += 1
'''
i = 0
while i < len(name_list):
    print(name_list[i])
    i += 1


# 1. i=0, i<3, True, print(name_list[0]) TOM
# 2. i=1, i<3, True, print(name_list[1]) Lily
# 3. i=2, i<3, True, print(name_list[2]) ROSE




