# 三角形： 每行星星的个数和行号数相等
j = 0
while j < 5:
    # 一行星星开始
    i = 0
    # i表示每行里面星星的个数，这个数字要和行号相等，所以i要和j联动
    while i <= j:
        print('*', end='')
        i += 1
    # 一行星星结束：换行显示下一行
    print()
    j += 1


