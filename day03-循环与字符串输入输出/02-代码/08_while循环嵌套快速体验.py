"""
1. 循环打印3次饿了，准备吃饭
2. 开始吃饭
3. 上面是一套循环，这一套循环要重复执行3此 -- 一套循环要重复执行 -- 放到一个while循环里面
"""
j = 0
while j < 3:
    i = 0
    while i < 3:
        print('饿了，准备吃饭')
        i += 1
    print('开始吃饭')
    print('一顿饭吃完结束-----------------------')
    j += 1
#
# 1.j==0, j<3, True, i=0, i<3, True, 饿了, i==1, i<3, True, 饿了, i==2, True, 饿了， i==3, False, 吃饭， 结束， j==1
# 2.j==1, j<3, True,
